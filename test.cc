void printStringCommaSeparated(const string &input, int groupLength) {
  int lastInd = groupLength - 1;
  for (int i = 0 ; i < input.length(); ++i) {
    cout << input.at(i);
    if (i % groupLength == lastInd) {
      cout << ", ";
    }
  }
  cout << endl;
}

